public class Calculator {
    public int add(int a, int b) {
        return a + b;
    }
    public int sub(int a, int b) {
        return a - b;
    }
    public int mul(int a, int b) {
        return a * b;
    }
    public int div(int a, int b) {
        return a / b;
    }

    public int square(int a) {
        return a*a;
    }
    public int cub(int a) {
        return a*a*a;
    }
    public int power(int a) {
        return a*a*a;
    }
}